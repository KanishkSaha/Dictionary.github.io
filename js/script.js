const ulTag = box.querySelector("ul"); // Ensure you're selecting the correct <ul> element
// Clear the list before appending new songs
ulTag.innerHTML = ""; 
// Loop through the `allMusic` array and create list items for each song
for (let i = 0; i < allMusic.length; i++) {
  // Create the HTML for the list item
  let liTag = `<li li-index="${i + 1}">
               <div class="row">
                 <span>${allMusic[i].name}</span>
                 <p>${allMusic[i].artist}</p>
               </div>
               <span id="music-${i}" class="audio-duration">--:--</span>
               <audio class="audio-element" src="songs/${allMusic[i].src}.mp3"></audio>
             </li>`;


  // Append the list item to the <ul>
  ulTag.insertAdjacentHTML("beforeend", liTag);

  // After inserting, select the audio element and add the event listener
  let liAudioDurationTag = ulTag.querySelector(`#music-${i}`);  // Using the loop index 'i'
  let liAudioTag = ulTag.querySelector(`audio[src="songs/${allMusic[i].src}.mp3"]`);  
  
  // Event listener to calculate and display the duration when the data is loaded
  liAudioTag.addEventListener("loadeddata", ()=> {
    let duration = liAudioTag.duration;
    let totalMin = Math.floor(duration / 60);
    let totalSec = Math.floor(duration % 60);
    if (totalSec < 10) {
      totalSec = `0${totalSec}`;
    }
    liAudioDurationTag.innerText = `${totalMin}:${totalSec}`;
    liAudioDurationTag.setAttribute("t-duration", `${totalMin}:${totalSec}`);
  });
}

//play particular song from the list onclick of li tag
function playingSong(){
  const allLiTag = ulTag.querySelectorAll("li");
  
  for (let j = 0; j < allLiTag.length; j++) {
    let audioTag = allLiTag[j].querySelector(".audio-duration");
    
    if(allLiTag[j].classList.contains("playing")){
      allLiTag[j].classList.remove("playing");
      let adDuration = audioTag.getAttribute("t-duration");
      audioTag.innerText = adDuration;
    }

    //if the li tag index is equal to the musicIndex then add playing class in it
    if(allLiTag[j].getAttribute("li-index") == musicIndex){
      allLiTag[j].classList.add("playing");
      audioTag.innerText = "Playing";
    }

    allLiTag[j].setAttribute("onclick", "clicked(this)");
  }
}

function clicked(element){
  let getLiIndex = element.getAttribute("li-index");
  musicIndex = getLiIndex; //updating current song index with clicked li index
  loadMusic(musicIndex);
  playMusic();
  playingSong();
}
